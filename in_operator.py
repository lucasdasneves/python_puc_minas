#in and not int operators

materiais = ['caneta', 'caderno', 'livro', 'e-book']
valor = 'caneta' in materiais
print (valor)
valor = 'agenda' in materiais
print (valor)

materiais = ['caneta', 'caderno', 'livro', 'e-book']
valor = 'caneta' not in materiais
print (valor)
valor = 'agenda' not in materiais
print (valor)
